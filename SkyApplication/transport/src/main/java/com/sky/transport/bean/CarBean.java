public class CarBean {
    private String diction_id;
    private String function_id;
    private String type;
    private String value;
    private String extend_01;
    private String extend_02;
    private String remark;

    public String getDiction_id() {
        return diction_id;
    }

    public void setDiction_id(String diction_id) {
        this.diction_id = diction_id;
    }

    public String getFunction_id() {
        return function_id;
    }

    public void setFunction_id(String function_id) {
        this.function_id = function_id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getExtend_01() {
        return extend_01;
    }

    public void setExtend_01(String extend_01) {
        this.extend_01 = extend_01;
    }

    public String getExtend_02() {
        return extend_02;
    }

    public void setExtend_02(String extend_02) {
        this.extend_02 = extend_02;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
